package com.pdp.shared.operation;

/**
 * 补偿策略（FR-168）。
 *
 * <p>对应 spec.md "高风险操作目录" 中"撤销、回退或补偿"列的策略分类。
 * 操作执行失败或主动触发补偿时，按此策略执行恢复。
 */
public enum CompensationStrategy {

    /**
     * 回滚：恢复操作前状态（仅未达不可逆点时可用）。
     *
     * <p>典型：领域包发布失败回滚到原生产版本；基线替换失败恢复原基线。
     */
    ROLLBACK,

    /**
     * 反向同步：将目标侧变更同步回源侧，恢复双向一致性（不可逆点后可用）。
     *
     * <p>典型：历史迁移/数据库切换在 PDP 已产生新业务写入后，将新写入反向同步回旧系统
     * （FR-149：除非已验证反向同步，否则不得直接恢复旧系统为写入主系统）。
     */
    REVERSE_SYNC,

    /**
     * 人工补偿：自动补偿不可行，由运维或业务负责人按运行手册执行（不可逆点后可用）。
     *
     * <p>典型：交付件已发布到下游系统并通知外部干系人，需人工沟通撤销或修订；
     * 数据处置已物理删除，需从备份恢复或按法律程序处理。
     */
    MANUAL,

    /**
     * 无补偿：操作不可逆且无补偿路径，操作者 MUST 显式确认接受风险。
     *
     * <p>典型：法律保留解除并物理删除且无备份的处置操作。
     * 审计 MUST 记录操作者明确确认不可逆且无补偿路径的事实。
     */
    NONE;

    /**
     * 是否可用于不可逆点之后。
     *
     * <p>{@link #ROLLBACK} 仅在未达不可逆点时可用；其余策略在不可逆点后可用。
     */
    public boolean availableAfterPointOfNoReturn() {
        return this != ROLLBACK;
    }
}
